    public void lmMoi(){
        txtTen.setText("");
        cbbLoai.setSelectedItem("KHTN");
        rdbMs.setSelected(true);
        txtGia.setText("");
        vitri=-1;
    }
    
    public void add(){
        Sach s = new Sach();
        s.setTen(txtTen.getText());
        s.setLoai((String) cbbLoai.getSelectedItem());
        if(rdbMs.isSelected()==true){
            s.setHang("Mới");
        }else{
            s.setHang("Cũ");
        }
        s.setGia(Double.parseDouble(txtGia.getText()));
        listSach.add(s);
    }
    
    public void fill(){
        model.setRowCount(0);
        for(Sach x: listSach){
            model.addRow(new Object[]{x.getTen(),x.getLoai(),x.getHang(),x.getGia(),x.danhGia()});
        }
        display(vitri);
    }
    
    public void display(int vitri){
        if(vitri>=0){
            txtTen.setText(listSach.get(vitri).getTen()); 
            cbbLoai.setSelectedItem(listSach.get(vitri).getLoai());
            if(listSach.get(vitri).getHang().equalsIgnoreCase("Mới")){
                rdbMs.setSelected(true);
            }else{
                rdbCu.setSelected(true);
            }
            txtGia.setText(listSach.get(vitri).getGia()+"");  
            lblHang.setText((vitri+1)+"/"+listSach.size());
            tblSach.setRowSelectionInterval(vitri, vitri);
        }else{
            lblHang.setText("...");
        }
    }
    
    public void xoa(){
        vitri= tblSach.getSelectedRow();
        try {            
            listSach.remove(vitri);
            model.removeRow(vitri);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "chọn dóng xóa");
        }
    }
    
    public void luu(){
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("sach.txt"));
            oos.writeObject(listSach);
            oos.flush();
            oos.close();
            JOptionPane.showMessageDialog(rootPane, "Lưu tc");
        } catch (Exception e) {
//            JOptionPane.showMessageDialog(rootPane, "Lưu thất bại");
        }       
    }
    
    public void mo(){
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream("sach.txt"));
            listSach = (List<Sach>) ois.readObject();
            ois.close();
            fill();
            JOptionPane.showMessageDialog(rootPane, "Mở tc");
        } catch (Exception e) {
//            JOptionPane.showMessageDialog(rootPane, "Mở thất bại");
        }
    }
    
    
