    public void chayChu(){
        new Thread(){
            public void run(){
                String chu = lbChu.getText()+" ";
                while (true) {                   
                   lbChu.setText(chu);
                   chu=chu.substring(1,chu.length())+chu.charAt(0);
                    try {
                        Thread.sleep(1000);
                    } catch (Exception e) {
                    }
                }
            }
        }.start();
    }
    
    public void chayMang(){
        new Thread(){
            public void run(){
                while (true) {
                   String chu = lbMang.getText();
                   String[] mang = chu.split(" ");
                   chu = mang[1]+" "+mang[2]+" "+mang[0];
                   lbMang.setText(chu);
                    try {
                        Thread.sleep(1000);
                    } catch (Exception e) {
                    }
                }
            }
        }.start();
    }
    
